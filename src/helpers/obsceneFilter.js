module.exports = function obsceneFilter(value) {
  if (!value || !value.length || value === 'null') {
    return '';
  }
  for (let i = 0; i < obsceneArray.length; i++) {
    if (value.toLowerCase().indexOf(obsceneArray[i]) > -1) {
      value = '[censored]';
    }
  }
  if (value.length > 128) {
    value = `${value.substr(0, 128)}...`;
  }

  return value;
};
